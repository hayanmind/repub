async function main() {
  marked.setOptions({ gfm: true, breaks: false });

  console.log(`\n→ ${TARGETS.length} PDF targets. Regenerating in place.\n`);

  const browser = await puppeteer.launch({ headless: true });
  let ok = 0;
  let fail = 0;
  const summary: { path: string; size: number; pages: number }[] = [];
  const t0 = Date.now();

  for (const t of TARGETS) {
    const absPdf = join(ROOT, t.pdfPath);
    const tmpMd = absPdf.replace(/\.pdf$/, '.regen.tmp.md');

    try {
      writeFileSync(tmpMd, t.md, 'utf8');

      const bodyHtml = await marked.parse(t.md);
      const fullHtml = TEMPLATE(t.title, bodyHtml);

      const page = await browser.newPage();
      await page.setContent(fullHtml, { waitUntil: 'networkidle0', timeout: 60000 });
      await page.pdf({
        path: absPdf as `${string}.pdf`,
        format: 'A4',
        printBackground: true,
        margin: { top: '15mm', right: '20mm', bottom: '15mm', left: '20mm' },
      });
      await page.close();

      // remove the temp MD — only the PDF should remain
      unlinkSync(tmpMd);

      const sz = statSync(absPdf).size;
      summary.push({ path: t.pdfPath, size: sz, pages: 0 });
      ok++;
      console.log(`  ✓ ${t.pdfPath}  (${(sz / 1024).toFixed(1)} KB)`);
    } catch (err) {
      fail++;
      console.log(`  ✗ ${t.pdfPath}  ${err instanceof Error ? err.message : err}`);
      try {
        unlinkSync(tmpMd);
      } catch {
        /* ignore */
      }
    }
  }

  await browser.close();
  console.log(
    `\n✓ Done in ${((Date.now() - t0) / 1000).toFixed(1)}s — ok=${ok} fail=${fail}`
  );

  console.log('\nSize summary:');
  for (const s of summary) {
    console.log(`  ${(s.size / 1024).toFixed(1).padStart(8)} KB   ${s.path}`);
  }
}

main().catch((err) => {
  console.error('FATAL:', err);
  process.exit(1);
});
